// ─── UTILS ───────────────────────────────────────────────────────────────────
const rgb       = (doc, [r, g, b]) => doc.setTextColor(r, g, b);
const fillRgb   = (doc, [r, g, b]) => doc.setFillColor(r, g, b);
const strokeRgb = (doc, [r, g, b]) => doc.setDrawColor(r, g, b);

function setFont(doc, style = "normal", size = 10) {
  doc.setFontSize(size);
  doc.setFont("helvetica", style);
}

